package operators;

public class EqualityOperators {
    public static void main(String[] args) {
        int firstNumber = 13;
        int secondNumber = 5;

        System.out.println(firstNumber == secondNumber);//false
        System.out.println(firstNumber != secondNumber);//true
        System.out.println(firstNumber >= secondNumber);//true
        System.out.println(firstNumber <= secondNumber);//false
    }
}
